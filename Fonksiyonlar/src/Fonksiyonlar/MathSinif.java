package Fonksiyonlar;

public class MathSinif {

	public static void main(String[] args) {
		// Math sýnýfý metotlarý.
		// Math.ceil(x) => bir büyük tam sayýya yuvarlama yapar.
		// Math.cos(x) => cos deðerine döner.
		// Math.sin(x) => sin deðerine döner.
		// Math.floor(x) => küçük tam sayýya yuvarlar.
		// Math.max(x,y) => büyük sayýyý verir.
		// Math.min(x,y) => küçük sayýyý verir.
		// Math.pow(3,5) => 3^5 üs alýr.
		// Math.random() => 1 ve 0 arasýnda rastgele deðer.
		// Math.round(x) => yakýn olan tam sayýya döner.
		// Math.abs(x) => mutlak deðer
		System.out.println(Math.ceil(1.1));
		System.out.println(Math.cos(30));
		System.out.println(Math.floor(1.1));
		System.out.println(Math.max(1.2, 1));
		System.out.println(Math.pow(3, 5));
		System.out.println(Math.round(1.4));
		System.out.println(Math.abs(-1));
		System.out.println(Math.sqrt(16));
	}

}
